public class Operations {


    public static int sum(int a, int b){
        System.out.println("method sum(int, int)");
        return a + b;
    }

    public static double sum(double a, double b) {
        System.out.println("method sum(double,double");
        return a + b;
    }


    public static double sum(int a, double b){
        System.out.println("method sum(int, double");
        return a + b;
    }

    public static double sum(double a, int b) {
        System.out.println("method sum(a, b");
        return a + b;
    }
}
